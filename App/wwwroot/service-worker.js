const CACHE_NAME = 'appplat-cache-v3';
const APP_SHELL = [
  '/',
  '/Login',
  '/manifest.webmanifest',
  '/favicon.ico',
  '/favicon.svg',
  '/icons/app-icon-192.png',
  '/icons/app-icon-512.png',
  '/ui/tailwindConfig.js',
  '/ui/MenuTree.js'
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(APP_SHELL))
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(
        keys
          .filter((key) => key !== CACHE_NAME)
          .map((key) => caches.delete(key))
      )
    )
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') {
    return;
  }

  const requestUrl = new URL(event.request.url);
  if (requestUrl.origin !== self.location.origin) {
    return;
  }

  // Avoid stale framework modules and dynamic Razor handlers.
  // Always use network for /res/* and requests with ?handler=...
  const hasHandler = requestUrl.searchParams.has('handler');
  const isRuntimeModule = requestUrl.pathname.startsWith('/res/');
  if (hasHandler || isRuntimeModule) {
    event.respondWith(fetch(event.request));
    return;
  }

  if (event.request.mode === 'navigate') {
    event.respondWith(
      fetch(event.request)
        .then((response) => {
          const copy = response.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
          return response;
        })
        .catch(() => caches.match(event.request).then((cached) => cached || caches.match('/')))
    );
    return;
  }

  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) {
        return cached;
      }

      return fetch(event.request)
        .then((response) => {
          if (!response || response.status !== 200 || response.type !== 'basic') {
            return response;
          }

          const copy = response.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
          return response;
        })
        .catch(() => caches.match('/favicon.ico'));
    })
  );
});
